public class Drawing {
    public int x;
    public int y;
    public int width;
    public int height;
    public int color;
    public int shape;

    public Drawing() {

    }

    public Drawing(int x, int y, int width, int height, int color, int shape) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
        this.color = color;
        this.shape = shape;
    }

}
